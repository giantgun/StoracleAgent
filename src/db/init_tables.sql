-- ============================================================
-- Organizations
-- ============================================================
create or replace table organizations (
  id uuid primary key default gen_random_uuid() references auth.users(id) on delete cascade,
  name text,
  business_email text,
  first_name text,
  last_name text,
  agent_inbox_id text unique,
  is_agent_active boolean default false,
  created_at timestamptz default now()
);

-- ============================================================
-- Wallets  (session key + USDT balance only — no fiat/hedge assets)
-- ============================================================
create or replace table wallets (
  id uuid primary key default gen_random_uuid(),
  organization_id uuid references organizations(id) on delete cascade,

  public_session_key_address text,
  non_custodial_wallet_address text,

  usdt_balance numeric default 0,

  -- Encrypted session key private key (EOA)
  encrypted_session_key text,

  -- Serialized permission string from user's frontend approval
  -- Generated by frontend via serializePermissionAccount
  -- Stored here so the agent can deserialize it later
  session_key_approval text,

  -- Zerodev policy config: JSONB storing supplier whitelist, spend caps, rate limits
  -- Mirrors the on-chain CallPolicy/GasPolicy/RateLimitPolicy for fast local verification
  policy_config jsonb default '{}'::jsonb,

  -- Address of the Kernel smart account (set when frontend saves first approval)
  -- Used to read on-chain USDT balance of the org's smart account
  smart_account_address text,

  created_at timestamptz default now()
);

-- ============================================================
-- Suppliers
-- ============================================================
create or replace table suppliers (
  id uuid primary key default gen_random_uuid(),
  organization_id uuid references organizations(id) on delete cascade,

  name text,
  email text,
  non_custodial_wallet_address text,

  -- Whether this supplier's wallet has been verified against Zerodev on-chain session policy
  is_verified_onchain boolean default false,
  last_verified_onchain_at timestamptz,

  created_at timestamptz default now()
);

-- ============================================================
-- Inventory items
-- ============================================================
create or replace table inventory_items (
  id uuid primary key default gen_random_uuid(),
  organization_id uuid references organizations(id) on delete cascade,

  name text not null,
  unit_name text,

  unit_sales_price_in_usdt numeric,
  expected_purchase_price_in_usdt numeric,

