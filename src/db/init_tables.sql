  for each row execute procedure set_updated_at();

-- ============================================================
-- confirm_inventory_fulfillment
-- When the user confirms goods have arrived, this function:
--   1. Marks the invoice_paid event as fulfilled
--   2. Decreases in_transit_quantity on the item
--   3. Increases quantity on the item
--   4. Inserts a restock event into inventory_events
-- ============================================================
create or replace function confirm_inventory_fulfillment(
  p_event_id uuid,
  p_item_id uuid,
  p_quantity integer
)
returns void
language plpgsql
as $$
begin
  -- Mark the invoice_paid event as fulfilled
  update inventory_events
  set metadata = jsonb_set(
    coalesce(metadata, '{}'::jsonb),
    '{fulfillment_status}',
    '"fulfilled"'::jsonb
  )
  where id = p_event_id;

  -- Move from in_transit to available stock
  update inventory_items
  set
    quantity = quantity + p_quantity,
    in_transit_quantity = greatest(0, in_transit_quantity - p_quantity)
  where id = p_item_id;

  -- Record a restock event so it shows in the inventory timeline
  insert into inventory_events (
    organization_id,
    inventory_item_id,
    event_type,
    quantity_change
  ) select
    organization_id,
    p_item_id,
    'restock',
    p_quantity
  from inventory_events
  where id = p_event_id
  limit 1;
end;
$$;

-- ============================================================
-- record_supplier_payment
-- After a payment confirms on-chain, this function:
--   1. Inserts an invoice_paid event into inventory_events
--   2. Increments in_transit_quantity on the inventory item
--   3. Marks the invoice as paid
-- ============================================================
create or replace function record_supplier_payment(
  p_organization_id uuid,
  p_inventory_item_id uuid,
  p_quantity integer,
  p_amount numeric,
  p_tx_hash text,
  p_supplier_id bigint default null,
  p_invoice_id bigint default null
)
returns void
language plpgsql
as $$
begin
  -- 1. Create the invoice_paid event
  insert into inventory_events (
    organization_id,
    inventory_item_id,
    event_type,
    quantity_change,
    price_per_unit,
    reference_type,
    metadata
  ) values (
    p_organization_id,
    p_inventory_item_id,
    'invoice_paid',
    p_quantity,
    p_amount / p_quantity,
    'invoice',
    jsonb_build_object('fulfillment_status', 'pending', 'transaction_hash', p_tx_hash)
  );

  -- 2. Increment in_transit on the item
  update inventory_items
  set in_transit_quantity = coalesce(in_transit_quantity, 0) + p_quantity
  where id = p_inventory_item_id;

  -- 3. Mark the invoice as paid (if it exists)
  if p_invoice_id is not null then
    update invoices
    set status = 'paid'
    where id = p_invoice_id and status = 'pending';
  end if;
end;
$$;

