  is_agent_active boolean default true,

  created_at timestamptz default now(),
  updated_at timestamptz default now()
);

-- ============================================================
-- Inventory events  (sale, restock, invoice_requested, invoice_received, invoice_paid, fulfilled)
-- ============================================================
create or replace table inventory_events (
  id uuid primary key default gen_random_uuid(),

  organization_id uuid references organizations(id) on delete cascade,
  inventory_item_id uuid references inventory_items(id) on delete cascade,

  event_type text not null,
  -- sale | restock | invoice_requested | invoice_received | invoice_paid | manual_adjustment | fulfilled

  quantity_change integer,
  price_per_unit numeric,

  reference_id uuid,
  reference_type text,
  -- invoice | manual

  metadata jsonb,
  -- use metadata.fulfillment_status: 'pending' | 'fulfilled' for invoice_paid events

  created_at timestamptz default now()
);

